#include <bits/stdc++.h>
using namespace std;

void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];
    int curr = 1;
    int maxi = 1;
    for (int i = 1; i < n; i++)
    {
        if (a[i] < a[i - 1])
        {

            curr = 1;
        }
        else
        {
            curr++;
        }
        maxi = max(maxi, curr);
    }
    cout << maxi << endl;
}

int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    solve();

    return 0;
}